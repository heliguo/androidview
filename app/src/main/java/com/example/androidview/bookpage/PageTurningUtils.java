package com.example.androidview.bookpage;

import android.view.View;

/**
 * @author lgh on 2021/3/12 11:22
 * @description
 */
public class PageTurningUtils {

    public static void turning(View firstView, View secondView) {
        if (firstView == null || firstView.getVisibility() != View.VISIBLE
                || secondView == null || secondView.getVisibility() != View.VISIBLE) {
            return;
        }



    }

}
